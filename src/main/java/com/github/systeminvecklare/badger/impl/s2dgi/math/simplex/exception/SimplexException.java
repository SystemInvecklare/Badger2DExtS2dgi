package com.github.systeminvecklare.badger.impl.s2dgi.math.simplex.exception;

public class SimplexException extends Exception {
	private static final long serialVersionUID = 6198362899629030995L;

	public SimplexException() {
		super();
	}

	public SimplexException(String message, Throwable cause) {
		super(message, cause);
	}

	public SimplexException(String message) {
		super(message);
	}

	public SimplexException(Throwable cause) {
		super(cause);
	}
}
